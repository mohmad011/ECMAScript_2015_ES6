/*

	New String Methods

	includes => string.includes(Search String, Position = 0)

	repeat => string.repeat(Number Of Repeats);

*/


let x = 'Video - Learn How To Learn';


console.log(x.includes('Video')); // This Mean ==> Do The String Is Content This ==> [Video] ??

console.log(x.includes('Learn' , 8)); // This Mean ==> Do The String Is Content This ==> [Learn] From Index [ 8 ] ??


console.log(x.repeat(10)); // This Mean ==> Do The String Is Content This ==> [Video] ??